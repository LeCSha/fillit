#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned long int	nb;
	unsigned int		i;
	int					signe;

	i = 0;
	nb = 0;
	signe = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == 43 || str[i] == 45)
	{
		if (str[i] == 45)
			signe = -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		nb = nb * 10 + (str[i] - 48);
		i++;
	}
	if (nb > 9223372036854775807)
		return ((signe == -1) ? (0) : (-1));
	return (nb * signe);
}
